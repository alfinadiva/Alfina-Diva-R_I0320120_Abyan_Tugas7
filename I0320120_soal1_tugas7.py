# Program dengan fungsi string capitalize() dan upper()
teks = 'selamat menunaikan ibadah puasa'
ucapan = teks.capitalize().upper()
print(ucapan)

# Program dengan fungsi len () dan upper()
teks = 'selamat menunaikan ibadah puasa'
print("panjang kata :".upper(), len(teks))

# Program dengan fungsi center() dan upper ()
teks = 'selamat menunaikan ibadah puasa'
ucapan = teks.center(80, '^').upper()
print(ucapan)